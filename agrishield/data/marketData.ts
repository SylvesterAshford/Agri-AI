// Helper functions
export function getLatestPrice(item: MarketItem): { low: number | null; high: number | null; date: string } | null {
  if (!item.prices || item.prices.length === 0) return null;
  const latest = item.prices[item.prices.length - 1];
  return { low: latest.low, high: latest.high, date: latest.date };
}

export function getPriceChange(item: MarketItem): { amount: number; percent: number; direction: 'up' | 'down' | 'same' } | null {
  if (!item.prices || item.prices.length < 2) return null;

  const first = item.prices[0];
  const last = item.prices[item.prices.length - 1];

  const firstAvg = (first.low || 0) + (first.high || 0);
  const lastAvg = (last.low || 0) + (last.high || 0);

  if (firstAvg === 0) return null;

  const amount = lastAvg - firstAvg;
  const percent = (amount / firstAvg) * 100;

  return {
    amount,
    percent: Math.abs(percent),
    direction: amount > 0 ? 'up' : amount < 0 ? 'down' : 'same'
  };
}

export function formatPrice(price: number | null): string {
  if (price === null) return '-';
  return price.toLocaleString('my-MM');
}

export function getUniqueItemCategories(): string[] {
  return CROP_CATEGORIES.filter(Boolean);
}

export function getPriceAtDate(item: MarketItem, dateIndex: number): PricePoint | null {
  if (!item.prices || dateIndex < 0 || dateIndex >= item.prices.length) return null;
  return item.prices[dateIndex];
}
